
public class School {
    private String name;
    private int kidsInvolved;

    public School(String name, int kidsInvolved) {
        this.name = name;
        this.kidsInvolved = kidsInvolved;
    }

    public String getName() {
        return name;
    }

    public int getKidsInvolved() {
        return kidsInvolved;
    }
}
